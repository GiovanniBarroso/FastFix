<template>
  <section class="py-20 bg-gradient-to-b from-white to-gray-100 dark:from-gray-900 dark:to-gray-800 min-h-screen">
    <div class="max-w-6xl mx-auto px-6">
      <!-- Botón volver -->
      <div class="mb-6">
        <BackButtonUser />
      </div>

      <!-- Título -->
      <div class="text-center mb-10">
        <h1 class="text-5xl font-extrabold mb-4 text-gray-900 dark:text-white">📍 Tus direcciones</h1>
        <p class="text-lg text-gray-600 dark:text-gray-300">
          Gestiona tus direcciones de envío y facturación asociadas a tu cuenta.
        </p>
      </div>

      <!-- Botón para añadir dirección -->
      <div class="flex justify-end mb-6">
        <button @click="showModal = true"
          class="bg-blue-600 hover:bg-blue-700 text-white px-4 py-2 rounded font-semibold transition">
          ➕ Añadir nueva dirección
        </button>
      </div>

      <!-- Lista de direcciones -->
      <div v-if="addresses.length" class="grid gap-4 md:grid-cols-2">
        <div v-for="address in addresses" :key="address.id"
          class="bg-white dark:bg-gray-800 border border-gray-200 dark:border-gray-700 p-4 rounded-xl shadow">
          <h2 class="text-lg font-semibold text-gray-900 dark:text-white mb-2">🏠 {{ address.nombre }}</h2>
          <p class="text-sm text-gray-700 dark:text-gray-300">{{ address.direccion }}</p>
          <p class="text-sm text-gray-700 dark:text-gray-300">
            {{ address.codigo_postal }} - {{ address.ciudad }}
          </p>
          <p class="text-sm text-gray-700 dark:text-gray-300">
            {{ address.provincia }}, {{ address.pais }}
          </p>
          <p class="text-sm text-gray-700 dark:text-gray-300">📞 {{ address.telefono_envio }}</p>
        </div>
      </div>

      <!-- Sin direcciones -->
      <div v-else class="flex flex-col items-center text-center text-gray-600 dark:text-gray-400 mt-16">
        <img src="/images/empty-address.png" alt="Sin direcciones" class="w-40 h-40 mb-6 opacity-70" />
        <p class="text-xl font-medium mb-2">Aún no tienes direcciones registradas</p>
        <p class="text-sm mb-4">Puedes añadir una dirección al realizar tu primer pedido.</p>
        <router-link to="/products"
          class="inline-block bg-blue-600 hover:bg-blue-700 text-white font-semibold px-5 py-2 rounded">
          Ir a la tienda
        </router-link>
      </div>

      <!-- Acceso a Fortify -->
      <div class="mt-16 space-y-3 text-center">
        <router-link to="/profile" class="inline-block text-blue-600 dark:text-blue-400 hover:underline font-medium">
          🧍 Editar datos de perfil (Fortify)
        </router-link>
        <br />
        <router-link to="/change-password"
          class="inline-block text-blue-600 dark:text-blue-400 hover:underline font-medium">
          🔑 Cambiar contraseña (Fortify)
        </router-link>
      </div>

      <!-- Modal para nueva dirección -->
      <NewAddressModal :visible="showModal" @close="showModal = false" @refresh="fetchAddresses" />
    </div>
  </section>
</template>

<script setup>
import { ref, onMounted } from 'vue'
import api from '@/services/api'
import BackButtonUser from '@/components/ui/BackButtonUser.vue'
import NewAddressModal from '@/components/modals/NewAddressModal.vue'

const addresses = ref([])
const showModal = ref(false)

const fetchAddresses = async () => {
  try {
    const { data } = await api.get('/user/addresses')
    addresses.value = data.addresses
  } catch (err) {
    console.error('❌ Error al cargar direcciones:', err)
  }
}

onMounted(fetchAddresses)
</script>
