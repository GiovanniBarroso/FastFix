        <form @submit.prevent="enviarConsulta" class="space-y-6">
          <div>
            <label class="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1">Nombre</label>
            <input
              v-model="form.nombre"
              type="text"
              required
              :class="inputClass(form.nombre)"
              class="w-full p-3 rounded-md border bg-gray-50 dark:bg-gray-700 text-gray-800 dark:text-white focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
          </div>

          <div>
            <label class="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1">Correo electrÃ³nico</label>
            <input
              v-model="form.email"
              type="email"
              required
              :class="inputClass(form.email)"
              class="w-full p-3 rounded-md border bg-gray-50 dark:bg-gray-700 text-gray-800 dark:text-white focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
