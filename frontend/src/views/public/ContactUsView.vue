<script setup>
import { ref } from 'vue'

const form = ref({
  nombre: '',
  email: '',
  mensaje: ''
})

const mensajeEnviado = ref(false)
const enviando = ref(false)
const intentadoEnviar = ref(false)

// Estilo condicional si el campo estÃ¡ vacÃ­o
const inputClass = (campo) =>
  `${!campo && intentadoEnviar.value ? 'border-red-500 dark:border-red-400' : 'border-gray-300 dark:border-gray-600'}`

const enviarConsulta = async () => {
  intentadoEnviar.value = true

  // ValidaciÃ³n bÃ¡sica
  if (!form.value.nombre.trim() || !form.value.email.trim() || !form.value.mensaje.trim()) {
    return
  }

  enviando.value = true

  try {
    const res = await fetch(`${import.meta.env.VITE_API_BASE_URL}/contact`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(form.value)
    })

    if (!res.ok) throw new Error('Error al enviar mensaje')

    mensajeEnviado.value = true
    form.value = { nombre: '', email: '', mensaje: '' }
  } catch (err) {
    console.error(err)
    alert('OcurriÃ³ un error al enviar tu mensaje. Intenta mÃ¡s tarde.')
  } finally {
    enviando.value = false
    setTimeout(() => (mensajeEnviado.value = false), 3000)
  }
}
</script>
